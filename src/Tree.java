public class Tree {
	private String treeSpecies;
	private String numberPlanted;
	
	/**
	 * @return the treeSpecies
	 */
	public String getTreeSpecies() {
		return treeSpecies;
	}

	/**
	 * @param treeSpecies
	 *            the treeSpecies to set
	 */
	public void setTreeSpecies(String treeSpecies) {
		this.treeSpecies = treeSpecies;
	}

	/**
	 * @return the numberPlanted
	 */
	public String getNumberPlanted() {
		return numberPlanted;
	}

	/**
	 * @param numberPlanted
	 *            the numberPlanted to set
	 */
	public void setNumberPlanted(String numberPlanted) {
		this.numberPlanted = numberPlanted;
	}
}
